//          (parent dari int & double)      5, 3.14, -99.00             |
// bool   | Boolean (nilai benar/salah)   | true, false                 |
// String | Teks (kumpulan karakter)      | 'Dicoding', 'Y', ''         |
// List   | Daftar nilai (array)          | [1, 2, 3], ['a', 'b', 'c']  |
// Map    | Pasangan key-value            | {'x': 4, 'y': 10}           |
